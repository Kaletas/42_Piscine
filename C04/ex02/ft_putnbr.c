#include <unistd.h>

void	ft_putnbr(int nb);
void	ft_putchar(long nbr);

void	ft_putchar(long nbr)
{
	write(1, &nbr, 1);
}

void	ft_putnbr(int nb)
{
	long	nbr;

	nbr = (long)nb;
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = -nbr;
	}
	if (nbr > 9)
		ft_putnbr(nbr / 10);
	ft_putchar(nbr % 10 + '0');
}
